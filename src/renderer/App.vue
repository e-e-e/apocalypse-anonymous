          <br>
          I survived a hurricane. My house didn't flood, but only barely. I'm a pretty serious prepper, but after living through this I've learned quite a bit.
            <ol>
                <li>Check and gather your light sources. Keep them in one place. Have enough D batteries to last a lifetime. Candles work too, but large lanterns are better. Also invest in headlamps.</li>
                <li>Get serious about storing gas for a nice generator. At least 30 gallons. Even though the storm is long gone we still can't get gas. A nice generator is your best investment. Check out the Honda's. If you live in an environment with extreme heat also invest in a small window AC that can be powered with your generator. Make sure you have all of the big cords and things you'll need to run this whole set-up. Practice that set-up now.</li>
                <li>Make friends with a few key neighbors if you haven't already done so. Not just for helping each other, but to have people to pass the time with. You can share information, drink together, the kids play together, and just in general people to commiserate with. We also pooled our perishable food at times to make complete meals.</li>
                <li>Make sure you can take care of yourself and your kids without help from your spouse. Learn how to run the generator, chop a hole in the roof, turn off the gas, and other various things.</li>
                <li>Make a plan for waste management. We have now gone 10 days without a trash pick-up.</li>
                <li>Pharmacy. Our grocery stores opened up days before our stand alone pharmacies. Once the grocery stores open back up, it can take about four hours to complete your trip. Have an account at a pharmacy that's located inside a grocery store with a drive thru! If you need medication you can drive thru and avoid waiting in that grocery store line.</li>
                <li>Gas. Just expect to not be able to get gas before, during, and after an emergency. They did sometimes have diesel.</li>
                <li>Keep your pantry stocked at all times. There is no reason not to keep a few months of canned and dry goods. Same with water. During a disaster the stores are closed, and then the store shelves are bare. They limit your purchases of eggs, milk, bread, and water... and that's if they have those things. Produce, lunch meat, regular meat, and cheese are hard to come by. Its been a week and I still can't get bread.</li>
                <li>Have a good quality set of walkie talkies. Should you have to evacuate, you can caravan out as a family. Each driver has a walkie, and you can signal each other to change lanes as a group and to make stops as a group. Cell phones will most likely not work.</li>
                <li>Prepare for natural disasters. So much prepping focuses on the extremes...civil unrest, the collapse of government, a pulse weapon that takes down the grid, a super volcano, etc. These things may happen, but there is a low probability. You're more likely to experience an earthquake or a flood or a hurricane or a tornado. Prepare for those things specifically.</li>
            </ol>
          A quick list of things that flew off the shelves and continue to fly off the shelves... water, bread, eggs, milk, produce, meat, dust masks, large buckets, contractors trash bags, flashlights, D batteries, gas cans, and gas.
          Lastly, have enough to help others. Be prepared to assist the elderly neighbor that can't evacuate, or the child that needs food, etc. When we got the call that they were desperate for supplies at the shelter, I was very thankful that I was prepped enough that I could help others. I mostly saw elderly folks, and lots and lots of children. Many had been rescued from homes that have never flooded. Helping others is extremely important for your morale and general attitude in a disaster.
