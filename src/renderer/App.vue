          Hey guys, thought it would be good to share my home checklist summary:
            <ul> </li>
              <li>Water: 15 gallons per person.</li>
              <li>Food: non-perishable, long shelf life, easy to make or ready to eat.</li>
              <li>Fire: storm proof matches, lighters, and a fire starter.</li>
              <li>Light: candles, crank or battery powered flashlights or lanterns.</li>
              <li>Heat: mostly from clothes and blankets, but also propane heaters and survival blankets.</li>
              <li>First aid: must be more than just a “boo-boo kit,” should handle more serious injuries.</li>
              <li>Medication: Tylenol, activated charcoal, digestive aids, prescriptions.</li>
              <li>Hygiene: hand sanitizer, camp soap, baby wipes.</li>
              <li>Communication: crank or solar powered NOAA radio, flares, whistle.</li>
              <li>Cash: small bills, as much as you can afford to stash.</li>
              <li>Documents: copy of deeds/titles, insurance policies, birth certificates, maps, etc.</li>
              <li>Tools: work gloves, wrench for your gas lines, zip ties, duct tape, sewing kit, etc.</li>
              <li>Self defense: depending on personal views, up to and including firearms and ammo.</li>
           </ul>
            Notice that this summary does not include a few things you commonly see in other emergency preparedness checklists. We assume you already have things like clothing, blankets, cooking utensils, feminine hygiene products, garbage bags, toilet paper, etc.
            Sometimes other checklists mix the home supplies pieces with the bug out bag evacuation pieces. We split them up because they are distinctly different uses, it helps prevent double dipping, and reduces the chance gear is scattered and unprepared.
